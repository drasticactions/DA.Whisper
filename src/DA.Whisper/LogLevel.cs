namespace DA.Whisper;

/// <summary>
/// Represents the log levels.
/// </summary>
public enum LogLevel
{
    /// <summary>
    /// Indicates an error log level.
    /// </summary>
    Error = 2,

    /// <summary>
    /// Indicates a warning log level.
    /// </summary>
    Warning = 3,

    /// <summary>
    /// Indicates an information log level.
    /// </summary>
    Info = 4,
}
